public class ItemLine {
    String name;
    int quantity;

    public ItemLine(String name, int quantity) {
        this.name = name;
        this.quantity = quantity;
    }

    public String getName() {
        //Put correct code here
        return name;
    }

    public int getQuantity() {
        //Put correct code here
        return quantity;
    }

    public void setQuantity(int quantity) {
        //Put correct code here
        this.quantity = quantity;
    }
}